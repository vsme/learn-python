# 集合会自动去掉重复的元素
colors = {"红", "绿", "蓝", "红", "蓝"}
print(colors)  # 输出：{'红', '绿', '蓝'}

s = {1, 2, 3}
s.add(4)             # {1,2,3,4}
s.update([3, 5, 6])  # {1,2,3,4,5,6}
s.discard(10)        # 删不存在的元素也不会报错
# s.remove(10)       # 不存在会 KeyError
x = s.pop()          # 随机弹出一个元素（因无序）

# 集合代数
a, b = {1,2,3}, {3,4,5}
a | b     # 并集 {1,2,3,4,5}
a & b     # 交集 {3}
a - b     # 差集 {1,2}
a ^ b     # 对称差 {1,2,4,5}

# 判断关系
{1,2} <= {1,2,3}     # 子集 True
{1,2,3}.issuperset({1,2})  # 超集 True

print(type({}))